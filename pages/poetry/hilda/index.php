        <h1>Hilda</h1>
        <p>
            Une fin de journée parmi tant d'autres,<br>
            Le soleil te souhaite une douce soirée.<br>
            Délicat, c’est le départ d’une fable estivale.<br>
            Promenade dans le Jardin de l’Esplanade,<br>
            Petite pause, l’éclairage devient agréable.<br>
            Tu aperçois quelques marches,<br>
            Petite pose, prise d’image devant l’Arsenal.<br>
            Ce diaporama s’intitulera “summer night”.<br>
        </p>
        <p>
            Petit colibri, ton ramage se rapporte t-il à ton plumage ?<br>
            La beauté de ton visage offrait bon présage.<br>
            Deviendrais-je un jour le premier personnage de ton ouvrage ?<br>
            La tête dans les nuages, j’ai peur de voir arriver l’orage.<br>
            Ce soir là, un mirage de toi débarquait de nulle part.<br>
            Scintillant tout en haut parmi les étoiles,<br>
            Hilda, le prénom qui résonna au fond de mon âme.<br>
        </p>
        <p>
            Gravir des montagnes serait-ce suffisant ?<br>
            Ton élégance atteint des sommets.<br>
            Du Mont Blanc jusqu’au Mont Liban,<br>
            Personne ne peut rester indifférent.<br>
        </p>
        <p>
            À la manière de ce romancier,<br>
            J’ai tendance à romancer les écrits.<br>
            De la Moselle au fleuve d’Adonis,<br>
            Aucun excès de zèle ne pourrait noyer cette idylle.<br>
            Une balade, des souvenirs, jamais tu n’oublieras la rive.<br>
        </p>
        <p>
            Des récits à ton effigie sur des feuilles volantes,<br>
            Ta photographie inscrite dans mon esprit.<br>
            Une escalade de désirs s’évadant de cette toile charmante,<br>
            À l’instar de cette égérie c’est la romance que tu inspires.<br>
        </p>
        <p>
            Des lettres qui te sont destinés, par milliers j’en écrirai.<br>
            Princesse d’Orient, me fais-tu confiance ?<br>
            Sagesse débordante d’un esprit étriqué.<br>
            Ces quelques lignes n’ont rien de flamboyantes,<br>
            Pourtant, ton regard est…<br>
            Une poignée de mots ne pourrait être suffisante.<br>
        </p>
        <p>
            Stupéfiante comme l’inversion des deux pôles,<br>
            Je ressens une émotion nouvelle sur mes épaules.<br>
            Lâcher de colombes,<br>
            Je pense à toi du soir jusqu’à l’aube.<br>
        </p>
        <p>
            -dias dany
        </p>
