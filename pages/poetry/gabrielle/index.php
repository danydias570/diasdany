        <h1>Gabrielle</h1>
        <p>
            Sur un visage se trouve un nez.<br>
            Il n'a apparemment pas fait exprès<br>
            De se trouver là, perdu comme un colon<br>
            Au coeur de l’Argentine. Deux yeux profonds<br>
            L'épaulent, et ces nuances d'espoir sont les espions.<br>
            Dans une plaine teint café ils semblent sourire.<br>
            Tout comme la bouche qui, quand elle ne s'étire,<br>
            Est embrassé par des lèvres à la mine fraîche et hydraté.<br>
            Ainsi ce visage, vêtu de cheveux noirs de jais,<br>
            Comme de simples pièces d'or, constitue un trésor.<br>
        </p>
        <p>
            Pendant de longs instants, j'ai admiré cette somptuosité.<br>
            J'ai voyagé jusqu’en Amérique,<br>
            J'ai imaginé sagesse, poèmes et acryliques,<br>
            Et puis j'ai entendu, une délicate poésie.<br>
            Un accent laissant rêveur le Français que je suis,<br>
            Lissant les R, égayant les É,<br>
            De manière naturelle et compliqué.<br>
        </p>
        <p>
            Il semble que le Maître s'est emmêlé les pinceaux.<br>
            Avait-il un peu bu lorsqu'il peignit ce tableau ?<br>
            Tâches de rousseur là, paupières taillés aux ciseaux !<br>
            Et pourtant tout cela n'a rien d'un Picasso.<br>
            Considérons le tout de ce portrait robot:<br>
            C'est une fille ! Elle a l'air discrète et sincère.<br>
            Son expression est pourtant fier et ouverte.<br>
            D'une manière satisfaite, parmi nous elle rayonne.<br>
            Si j’étais désorienté, elle serait ma boussole,<br>
            Sans doute, car son regard est une pommade,<br>
            Contre les blessures qui font les jours maussades.<br>
        </p>
        <p>
            C'est moins une sirène qu'une Joconde.<br>
            Les atouts imparables et son regard envoûtants,<br>
            Sont des pétales froides, des beautés profondes.<br>
            Face à la douceur de ce visage déroutant,<br>
            L'auteur n'a pas laissé sa signature au coin de l'oeuvre.<br>
            Sait-il que celle-ci est certainement son chef-d'oeuvre ?<br>
            J'en doute, et discrètement, tant que tu l'ignorera,<br>
            Je penserais à toi, ma Mona Lisa.<br>
        </p>
        <p>
            -dias dany
        </p>
